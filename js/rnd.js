/**
 * 140p Portfolio - R&D Page JavaScript
 */

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', () => {
    // Animate R&D cards on scroll
    const rndCards = document.querySelectorAll('.rnd-card');
    
    const rndObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.classList.add('visible');
                }, index * 100);
                rndObserver.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    });
    
    rndCards.forEach(card => rndObserver.observe(card));
    
    // Animate experiment items
    const experimentItems = document.querySelectorAll('.experiment-item');
    
    const expObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }, index * 50);
                expObserver.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -30px 0px'
    });
    
    experimentItems.forEach(item => {
        item.style.opacity = '0';
        item.style.transform = 'translateY(30px)';
        item.style.transition = 'opacity 0.6s cubic-bezier(0.16, 1, 0.3, 1), transform 0.6s cubic-bezier(0.16, 1, 0.3, 1)';
        expObserver.observe(item);
    });
});
